export function isInsulatedWire(wireTypeId: WireTypeId): boolean {
  return wireTypeId === 'IV' || wireTypeId === 'HIV'
}

export function getConditionSummary(
  method: InstallationMethod,
  wireCount: WireCount,
  wireTypeId: WireTypeId,
): string {
  return isInsulatedWire(wireTypeId) && method === '配管内'
    ? `${method}・${wireCount}`
    : method
}

export function getAllowableCurrentForSpec(
  wireTypeId: WireTypeId,
  method: InstallationMethod,
  wireCount: WireCount,
  spec: WireSpec,
): number | undefined {
  let dataCondition: string

  if (isInsulatedWire(wireTypeId)) {
    if (method === 'ころがし') {
      dataCondition = spec.sizeUnit === 'mm' ? '気中（単線）' : '気中（より線）'
    } else {
      dataCondition = `管内（${wireCount}）`
    }
  } else if (wireTypeId === 'VVF' || wireTypeId === 'VVR') {
    if (method !== 'ころがし') return undefined
    if (spec.coreLabel !== '2心' && spec.coreLabel !== '3心') return undefined
    dataCondition = `気中（${spec.coreLabel}）`
  } else if (wireTypeId === 'CV') {
    if (!spec.coreLabel || spec.coreLabel === '4心') return undefined
    const internalMethod = method === 'ころがし' ? '気中暗渠' : '管路引入れ'
    dataCondition = `${internalMethod}（${spec.coreLabel}）`
  } else {
    dataCondition = method === 'ころがし' ? '気中暗渠' : '管路引入れ'
  }

  return DATA[wireTypeId]?.[dataCondition]?.[spec.sizeText]
}
